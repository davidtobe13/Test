// for addition
const add = (firstNumber, secondNumber)=>{
    const result = firstNumber+secondNumber
    return result
}

// for subtraction
const sub = (firstNumber, secondNumber)=>{
    const result = firstNumber+secondNumber
    return result
}

// for multiplication
const multiply = (firstNumber, secondNumber)=>{
    const result = firstNumber*secondNumber
    return result
}

// for division
const div = (firstNumber, secondNumber)=>{
    const result = firstNumber/secondNumber
    return result
}

module.exports = {
    add,
    sub,
    multiply,
    div
}
